public class Main
{
    public static void main(String[] args)
    {
        // Math.max() returns the maximum value
        System.out.println(Math.max(10,4));
        
        // Math.min() returns the minimum value
        System.out.println(Math.min(78,10));
        
        // sqrt() to find the square root of a number
        System.out.println(Math.sqrt(99));
        
        // returns the abstract(positive) value of the given number.
        System.out.println(Math.abs(-8.29845));
    }
}
