package g;

public class DoWhile {
	public static void main(String[] args)
	{
		int i=0;
		do
		{
			System.out.println("Monday");
			i++;
		}
		while(i<10);
		int count=5;
		while(count>0)
		{
			System.out.println(count);
			count--;
		}
		System.out.println("Haaaapppyy Newww Year!!!!!!!!!!");
	}

}
